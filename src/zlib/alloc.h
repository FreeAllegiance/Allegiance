#pragma once

#define _DUMP_LEAKS_

inline void InitAllocs() {}

#define ArrayCount(parray) (sizeof(parray) / sizeof(*parray))

//////////////////////////////////////////////////////////////////////////////
//
// Subclasses of TZeroFill are filled with zeros on construction
//
//////////////////////////////////////////////////////////////////////////////

template<class Type>
class TZeroFill : public Type {
protected:
    TZeroFill()
    {
        Reinitialize();
    }

public:
    void Reinitialize()
    {
        memset(this, 0, sizeof(Type));
    }
};